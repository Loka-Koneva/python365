# За чистый интернет
#
# Яндекс всегда пытается показывать креативную рекламу интересную для данного пользователя. Но, к сожалению, это выходит не всегда. Мальчик Петя является идейным марксистом и не хочет, чтобы крупные акулы капитала зарабатывали на нём деньги. Для этого он завёл себе правило каждый раз, заходя на новый сайт, банить ровно по одному рекламному баннеру.
#
# У вас есть история перемещений Пети по Интернету, представленная в виде NN URL-ов сайтов, которые он посещал. Ваша задача состоит в том, чтобы определить сколько есть забанненых баннеров на сайтах, имеющих своим URL-префиксом данную последовательность символов.
# Формат ввода
#
# На вход вам вводится 1≤N≤2∗1031≤N≤2∗103 сайтов которые посетил Петя. Далее, каждый на отдельной строке, вводятся URL-ы, состоящие из доменного имени и пути (возможно пустым). После вводятся число 1≤M≤1051≤M≤105, количество запросов. Потом идут сами запросы, каждый на отдельной строке, состоящие из последовательности символов.
#
# URL и запрос состоит только из прописных латинских букв, точек и слешей и его длина 1≤ki≤501≤ki​≤50. Оканчивается URL и запрос всегда на латинскую букву.
# Формат вывода
#
# Для каждого из MM запросов на отдельной строке выведите количество забаненных баннеров, размещённых на сайтах, имеющих своим URL-префиксом данную последовательность символов.
